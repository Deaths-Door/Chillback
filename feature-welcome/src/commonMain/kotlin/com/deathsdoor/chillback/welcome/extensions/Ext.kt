package com.deathsdoor.chillback.welcome.extensions

fun String.matchesEmail() = emailAddressRegex.matches(this)

private val emailAddressRegex by lazy {
    Regex(
        "[a-zA-Z0-9\\+\\.\\_\\%\\-\\+]{1,256}" +
                "\\@" +
                "[a-zA-Z0-9][a-zA-Z0-9\\-]{0,64}" +
                "(" +
                "\\." +
                "[a-zA-Z0-9][a-zA-Z0-9\\-]{0,25}" +
                ")+"
    )
}